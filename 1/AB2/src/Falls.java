
public class Falls {
	public static void main(String[] args) {
		int i = 50;
		
		if (i < 100) {
			System.out.println("Wert ist kleiner als 100");
		}
		if (i >= 40) {
			System.out.println("Wert ist groesser oder gleich 40");
		}
		if (i == 50) {
			System.out.println("Wert ist genau 50");
		}
		if (i <= 30) {
			System.out.println("Wert ist kleiner oder gleich 30");
		}
	}
}
